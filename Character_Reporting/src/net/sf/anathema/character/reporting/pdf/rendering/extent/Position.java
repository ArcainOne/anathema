package net.sf.anathema.character.reporting.pdf.rendering.extent;

public class Position {

  public final float x;
  public final float y;

  public Position(float x, float y) {
    this.x = x;
    this.y = y;
  }

  public float getX() {
    return x;
  }

  public float getY() {
    return y;
  }
}
